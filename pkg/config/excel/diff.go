package excel

/*
在Go语言中操作Excel文件，可以使用以下几个三方库：

excelize
excelize是一个Go语言的Excel文件操作库，可以创建、读取和编辑.xlsx格式的Excel文件。它支持多种数据类型和格式，并提供了丰富的API接口。

安装命令：

bash
go get github.com/360EntSecGroup-Skylar/excelize/v2
go-xlsx
go-xlsx是一个基于Go语言的Excel文件操作库，支持读取和编辑.xlsx格式的Excel文件。它提供了简单易用的API接口，支持多种数据类型和格式。

安装命令：

bash
go get github.com/tealeg/xlsx
unioffice
unioffice是一个基于Go语言的Office文档操作库，支持读取和编辑.xlsx、.docx和.pptx等多种格式的Office文档。它提供了灵活的API接口，支持多种数据类型和格式。

安装命令：

bash
go get github.com/unidoc/unioffice/...
以上三个库都提供了良好的文档和示例，可以根据实际需要选择适合自己的库进行使用。需要注意的是，这些库都不支持读取或编辑.xls格式的Excel文件，因为.xls格式已经过时，应该使用.xlsx格式代替。

*/
